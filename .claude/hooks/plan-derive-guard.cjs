#!/usr/bin/env node
'use strict';
/**
 * PostToolUse hook — warns when a GENERATED plan file is hand-edited.
 *
 * Files produced by plan-derive.cjs carry a generated banner; their source of truth
 * is decisions.log.jsonl, so hand-edits are lost on the next derive. This hook fires
 * ONLY for files carrying that banner, so plans that don't use the derive engine are
 * untouched. Always fail-open: returns { continue: true } on any error.
 */

const { createHookTimer, logHookCrash } = require('./lib/hook-logger.cjs');

// Must match the GENERATED_MARKER prefix in ../scripts/lib/plan-engine.cjs
const GENERATED_MARKER = '<!-- @generated by plan-derive from decisions.log.jsonl';

let input = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (chunk) => {
  input += chunk;
});
process.stdin.on('end', () => {
  const timer = createHookTimer('plan-derive-guard', { event: 'PostToolUse' });
  try {
    if (!input.trim()) {
      timer.end({ status: 'skip', exit: 0, note: 'empty-input' });
      process.stdout.write(JSON.stringify({ continue: true }));
      return;
    }
    const data = JSON.parse(input);
    const toolName = data.tool_name || '';
    const filePath = data.tool_input?.file_path || data.tool_input?.path || '';
    const fs = require('fs');

    if (!filePath.endsWith('.md') || !fs.existsSync(filePath)) {
      timer.end({ tool: toolName, status: 'skip', exit: 0, note: 'non-md-or-missing' });
      process.stdout.write(JSON.stringify({ continue: true }));
      return;
    }

    // Read only the head — the banner is on line 1 (plan.md) or just after frontmatter (phase files).
    const head = fs.readFileSync(filePath, 'utf8').slice(0, 400);
    if (!head.includes(GENERATED_MARKER)) {
      timer.end({ tool: toolName, status: 'skip', exit: 0, note: 'not-generated' });
      process.stdout.write(JSON.stringify({ continue: true }));
      return;
    }

    const warning = [
      '[Plan Engine] This file is GENERATED from decisions.log.jsonl — hand-edits are lost on the next derive.',
      'Change it through the log instead:',
      '  node .claude/scripts/plan-log-append.cjs <plan-dir> \'{"kind":"decision","phase":N,"title":"...","body":"..."}\'',
      '  node .claude/scripts/plan-derive.cjs <plan-dir>',
    ].join('\n');

    timer.end({ tool: toolName, status: 'warn', exit: 0, target: 'generated-plan-file' });
    process.stdout.write(JSON.stringify({ continue: true, additionalContext: warning }));
  } catch (err) {
    logHookCrash('plan-derive-guard', err, { event: 'PostToolUse' });
    process.stdout.write(JSON.stringify({ continue: true }));
  }
});
